const state = {
  activeFilter: "all"
};

function normalizeItem(item) {
  return {
    ...item,
    technologies: Array.isArray(item.technologies) ? item.technologies : [],
    tags: Array.isArray(item.tags) ? item.tags : [],
    button: {
      label: item.button?.label || "Open project",
      href: item.button?.href || "#"
    }
  };
}

function getAllProjects(data) {
  return data.categories.flatMap(category =>
    category.items.map(item => ({
      ...normalizeItem(item),
      categoryName: category.name,
      categorySlug: category.slug
    }))
  );
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function createPill(label, slug, isActive) {
  return `
    <button class="filter-pill${isActive ? " is-active" : ""}" type="button" data-filter="${escapeHtml(slug)}">
      ${escapeHtml(label)}
    </button>
  `;
}

function renderFilterPills(data) {
  const root = document.getElementById("filterPills");
  if (!root) return;

  root.innerHTML = [
    createPill("All", "all", state.activeFilter === "all"),
    ...data.categories.map(category =>
      createPill(category.name, category.slug, state.activeFilter === category.slug)
    )
  ].join("");

  root.querySelectorAll("[data-filter]").forEach(button => {
    button.addEventListener("click", () => {
      state.activeFilter = button.dataset.filter;
      renderFilterPills(data);
      renderSections(data);
    });
  });
}

function renderList(items, className) {
  if (!items.length) return "";
  return `
    <ul class="${className}" role="list">
      ${items.map(item => `<li>${escapeHtml(item)}</li>`).join("")}
    </ul>
  `;
}

function renderButton(button) {
  const href = button.href || "#";
  const isExternal = href.startsWith("http");
  const attrs = isExternal ? ' target="_blank" rel="noopener noreferrer"' : "";
  return `<a class="card-link" href="${escapeHtml(href)}"${attrs}>${escapeHtml(button.label)}</a>`;
}

function renderCard(item) {
  return `
    <article class="project-card">
      <div class="project-card__top">
        <p class="project-card__meta">${escapeHtml(item.categoryName)} · ${escapeHtml(item.time)}</p>
        <h3 class="project-card__title">${escapeHtml(item.title)}</h3>
      </div>
      <div class="project-card__body">
        ${renderList(item.technologies, "tech-list")}
        ${renderList(item.tags, "tag-list")}
      </div>
      <div class="project-card__actions">
        ${renderButton(item.button)}
      </div>
    </article>
  `;
}

function renderSection(category) {
  const items = category.items.map(normalizeItem);
  return `
    <section class="work-section" id="${escapeHtml(category.slug)}">
      <div class="section-head">
        <div>
          <span class="section-label">${escapeHtml(category.slug)}</span>
          <h2 class="section-title">${escapeHtml(category.name)}</h2>
        </div>
        <p class="section-copy">${items.length} project${items.length === 1 ? "" : "s"} in this category.</p>
      </div>
      <div class="projects-grid">
        ${items
          .map(item =>
            renderCard({
              ...item,
              categoryName: category.name,
              categorySlug: category.slug
            })
          )
          .join("")}
      </div>
    </section>
  `;
}

function renderSections(data) {
  const root = document.getElementById("sectionsRoot");
  if (!root) return;

  const categories =
    state.activeFilter === "all"
      ? data.categories
      : data.categories.filter(category => category.slug === state.activeFilter);

  root.innerHTML = categories.map(renderSection).join("");
}

function renderHeroPreview(data) {
  const root = document.getElementById("heroPreview");
  if (!root) return;

  const featured = getAllProjects(data).slice(0, 6);
  root.innerHTML = `
    <div class="preview-stack">
      ${featured
        .map(
          item => `
            <div class="preview-item">
              <span>${escapeHtml(item.title)}</span>
              <strong>${escapeHtml(item.time)}</strong>
            </div>
          `
        )
        .join("")}
    </div>
  `;
}

function animateStat(element, endValue) {
  const duration = 900;
  const start = performance.now();

  function tick(now) {
    const progress = Math.min((now - start) / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    element.textContent = Math.round(endValue * eased);
    if (progress < 1) requestAnimationFrame(tick);
  }

  requestAnimationFrame(tick);
}

function renderStats(data) {
  const totalProjects = getAllProjects(data).length;
  const totalCategories = data.categories.length;

  const projectsEl = document.getElementById("stat-projects");
  const categoriesEl = document.getElementById("stat-categories");

  if (projectsEl) animateStat(projectsEl, totalProjects);
  if (categoriesEl) animateStat(categoriesEl, totalCategories);
}

function initThemeToggle() {
  const root = document.documentElement;
  const toggle = document.querySelector("[data-theme-toggle]");
  if (!toggle) return;

  let theme = root.getAttribute("data-theme") || "dark";
  root.setAttribute("data-theme", theme);

  const setIcon = () => {
    toggle.setAttribute("aria-label", `Switch to ${theme === "dark" ? "light" : "dark"} mode`);
    toggle.innerHTML =
      theme === "dark"
        ? '<svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" aria-hidden="true"><path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z"></path></svg>'
        : '<svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" aria-hidden="true"><circle cx="12" cy="12" r="5"></circle><path d="M12 1v2M12 21v2M4.22 4.22l1.42 1.42M18.36 18.36l1.42 1.42M1 12h2M21 12h2M4.22 19.78l1.42-1.42M18.36 5.64l1.42-1.42"></path></svg>';
  };

  setIcon();

  toggle.addEventListener("click", () => {
    theme = theme === "dark" ? "light" : "dark";
    root.setAttribute("data-theme", theme);
    setIcon();
  });
}

function initPortfolio() {
  renderFilterPills(portfolioData);
  renderSections(portfolioData);
  renderHeroPreview(portfolioData);
  renderStats(portfolioData);
  initThemeToggle();
}

document.addEventListener("DOMContentLoaded", initPortfolio);

